/**
 * One-time / on-demand: project GADM Ghana admin1 GeoJSON into SVG path `d` strings for the homepage map.
 * Run: node scripts/generate-ghana-regions-map-paths.mjs
 * Source: scripts/data/gadm41_GHA_1.json (GADM 4.1).
 */
import fs from "node:fs";
import simplify from "@turf/simplify";
import { geoMercator, geoPath as geoPathFactory } from "d3-geo";

const WIDTH = 560;
const HEIGHT = 640;
const PAD = 16;

/** GADM NAME_1 → display name in `ghanaRegionsData` */
const GADM_TO_APP_NAME = {
  GreaterAccra: "Greater Accra",
  BonoEast: "Bono East",
  NorthEast: "North East",
  UpperEast: "Upper East",
  UpperWest: "Upper West",
  WesternNorth: "Western North",
  Ahafo: "Ahafo",
  Ashanti: "Ashanti",
  Bono: "Bono",
  Central: "Central",
  Eastern: "Eastern",
  Northern: "Northern",
  Oti: "Oti",
  Savannah: "Savannah",
  Volta: "Volta",
  Western: "Western",
};

const geoPath = new URL("./data/gadm41_GHA_1.json", import.meta.url);
const outPath = new URL("../src/lib/geo/ghana-region-map-paths.generated.ts", import.meta.url);

const raw = JSON.parse(fs.readFileSync(geoPath, "utf8"));
/** Reduce vertex count for a small client-safe module (visual is still region-accurate at homepage scale). */
const data = {
  ...raw,
  features: raw.features.map((f) => simplify(f, { tolerance: 0.018, highQuality: false })),
};
const projection = geoMercator().fitExtent(
  [
    [PAD, PAD],
    [WIDTH - PAD, HEIGHT - PAD],
  ],
  data,
);
const pathGen = geoPathFactory(projection);

const paths = {};
for (const f of data.features) {
  const gadm = f.properties.NAME_1;
  const appName = GADM_TO_APP_NAME[gadm];
  if (!appName) throw new Error(`Unknown GADM NAME_1: ${gadm}`);
  const d = pathGen(f);
  if (!d) throw new Error(`Empty path for ${gadm}`);
  paths[appName] = d;
}

const header = `/**
 * Auto-generated by scripts/generate-ghana-regions-map-paths.mjs — do not edit by hand.
 * ViewBox matches projection fit used in the generator.
 */
`;

const body = `export const GHANA_REGION_MAP_VIEWBOX = "${[0, 0, WIDTH, HEIGHT].join(" ")}" as const;

export const GHANA_REGION_MAP_PATHS: Record<string, string> = {
${Object.entries(paths)
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([name, d]) => `  ${JSON.stringify(name)}: ${JSON.stringify(d)},`)
  .join("\n")}
};
`;

fs.writeFileSync(outPath, header + body, "utf8");
console.log("Wrote", outPath.pathname, Object.keys(paths).length, "paths");
